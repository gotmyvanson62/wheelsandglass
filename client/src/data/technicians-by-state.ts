// Seeded random for consistent results per city
function seededRandom(seed: string): () => number {
  let hash = 0;
  for (let i = 0; i < seed.length; i++) {
    const char = seed.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash;
  }

  return function() {
    hash = Math.sin(hash) * 10000;
    return hash - Math.floor(hash);
  };
}

function generatePhone(random: () => number): string {
  const areaCode = Math.floor(random() * 800) + 200;
  const prefix = Math.floor(random() * 900) + 100;
  const line = Math.floor(random() * 9000) + 1000;
  return `(${areaCode}) ${prefix}-${line}`;
}

export function generateTechnicianList(
  city: string,
  state: string,
  count: number
): TechnicianInfo[] {
  const seed = `${state}-${city}`;
  const random = seededRandom(seed);

  // Cap at reasonable number of technicians to display
  const displayCount = Math.min(count, 12);

  const technicians: TechnicianInfo[] = [];
  const usedNames = new Set<string>();

  for (let i = 0; i < displayCount; i++) {
    // Generate unique name
    let name: string;
    let attempts = 0;
    do {
      const firstNameIndex = Math.floor(random() * firstNames.length);
      const lastNameIndex = Math.floor(random() * lastNames.length);
      name = `${firstNames[firstNameIndex]} ${lastNames[lastNameIndex]}`;
      attempts++;
    } while (usedNames.has(name) && attempts < 50);

    usedNames.add(name);

    // Determine status based on position and randomness
    // First few are more likely to be available
    // 60% available, 25% busy, 15% offline
    let status: 'available' | 'busy' | 'offline';
    const statusRoll = random();
    if (statusRoll < 0.60) {
      status = 'available';
    } else if (statusRoll < 0.85) {
      status = 'busy';
    } else {
      status = 'offline';
    }

    // Pick specialty
    const specialtyIndex = Math.floor(random() * specialties.length);

    technicians.push({
      name,
      specialty: specialties[specialtyIndex],
      status,
      phone: generatePhone(random),
    });
  }

  // Sort by status: available first, then busy, then offline
  const statusOrder = { available: 0, busy: 1, offline: 2 };
  technicians.sort((a, b) => statusOrder[a.status] - statusOrder[b.status]);

  return technicians;
}

